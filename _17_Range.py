for i in range(1, 11):
    print("Square of {0} is : {1}".format(i, i ** 2))

print()

# this is skipping even places
for i in range(1, 11, 2):
    print("Square of {0} is : {1}".format(i, i ** 2))

print()

# Reverse order printing using range
for i in range(10,0,-1):
    print("Square of {0} is : {1}".format(i, i ** 2))

